CREATE TABLE Staging_Player_Stats(
	Season INT,
	Event VARCHAR(250),
	Team VARCHAR(250),
	Player VARCHAR(250),
	Games_Played INT,
	Wins INT,
	Losses INT,
	Win_Rate FLOAT,
	Kills INT,
	Deaths INT,
	Assists INT,
	KDA_Ratio FLOAT,
	CS INT,
	CS_Min FLOAT,
	Gold INT,
	Gold_Min FLOAT,
	Damage FLOAT,
	Damage_Min FLOAT,
	Kill_Part FLOAT,
	Kill_Share FLOAT,
	Gold_Share FLOAT
);

INSERT INTO Staging_Player_Stats
SELECT
	season,
	event,
	team,
	player,
	games_played,
	wins,
	loses,
	win_rate,
	kills,
	deaths,
	assists,
	kill_death_assist_ratio,
	creep_score,
	cs_min,
	gold,
	gold_min,
	damage,
	damage_min,
	kill_participation,
	kill_share,
	gold_share
FROM [Player_Stats]